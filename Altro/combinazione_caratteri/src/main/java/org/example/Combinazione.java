package org.example;

public class Combinazione {
    public static void main(String[] args) {
        // creazione variabili di tipo char per eseguire la combinazione tra due caratteri
        char prima = 'a';
        char seconda = 'b';
        // eseguiamo l'operazione logica e stampiamo
        String somma = "" + prima+seconda;
        System.out.println(somma);
    }
}